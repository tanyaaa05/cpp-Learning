#include<iostream>
using namespace std;
int main() {
    int n=5;
    for(int i=0;i<n;i++){
        cout<<"Tanya"<<" ";
        break;
    }
    cout<<"Singh";
}


// The break in C++ is a loop control statement that is used to terminate the loop.
// As soon as the break statement is encountered from within a loop, 
// the loop iterations stop there and control returns from the loop immediately 
// to the first statement after the loop.